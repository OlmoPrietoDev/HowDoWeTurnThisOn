#ifndef __UTILS_H__
#define __UTILS_H__

struct Vec2 {
  float x;
  float y;

  Vec2(float _x, float _y) {
    x = _x;
    y = _y;
  }
};

struct Rect {
  float width;
  float height;

  Rect(float _width, float _height) {
    width = _width;
    height = _height;
  }
};

struct Color {
  unsigned char R;
  unsigned char G;
  unsigned char B;
  unsigned char A;

  Color(unsigned char _R, unsigned char _G, 
    unsigned char _B, unsigned char _A) {
    R = _R;
    G = _G;
    B = _B;
    A = _A;
  }
};

#endif // __UTILS_H__
